  function normalizeLanguage(value) {
    const language = String(value || '').trim().toLocaleLowerCase();
    if (language.startsWith('en')) return 'en-US';
    if (language.startsWith('zh')) return 'zh-CN';
    return DEFAULT_LANGUAGE;
  }

  function interpolate(template, values = {}) {
    return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => (
      Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match
    ));
  }

  function createI18n(options = {}) {
    const storage = options.storage;
    const documentRef = options.document;
    let savedLanguage = null;
    try { savedLanguage = storage?.getItem(STORAGE_KEY); } catch (error) { /* ignore */ }
    let activeLanguage = normalizeLanguage(savedLanguage || options.navigatorLanguage);

    const t = (key, values = {}, fallback = key) => {
      const template = MESSAGES[activeLanguage]?.[key]
        ?? MESSAGES[DEFAULT_LANGUAGE]?.[key]
        ?? fallback;
      return interpolate(template, values);
    };

    const valuesForNode = node => Object.fromEntries(
      Object.entries(node?.dataset || {})
        .filter(([key]) => /^i18n[A-Z]/.test(key)
          && !['i18nPlaceholder', 'i18nTitle', 'i18nAriaLabel'].includes(key))
        .map(([key, value]) => [key.charAt(4).toLocaleLowerCase() + key.slice(5), value]),
    );

    const apply = (rootNode = documentRef) => {
      if (!rootNode) return activeLanguage;
      if (documentRef?.documentElement) documentRef.documentElement.lang = activeLanguage;
      rootNode.querySelectorAll?.('[data-i18n]').forEach(node => {
        node.textContent = t(node.dataset.i18n, valuesForNode(node), node.textContent);
      });
      rootNode.querySelectorAll?.('[data-i18n-placeholder]').forEach(node => {
        node.placeholder = t(node.dataset.i18nPlaceholder, valuesForNode(node), node.placeholder);
      });
      rootNode.querySelectorAll?.('[data-i18n-title]').forEach(node => {
        node.title = t(node.dataset.i18nTitle, valuesForNode(node), node.title);
      });
      rootNode.querySelectorAll?.('[data-i18n-aria-label]').forEach(node => {
        node.setAttribute(
          'aria-label',
          t(node.dataset.i18nAriaLabel, valuesForNode(node), node.getAttribute?.('aria-label') || ''),
        );
      });
      return activeLanguage;
    };

    const setLanguage = value => {
      activeLanguage = normalizeLanguage(value);
      try { storage?.setItem(STORAGE_KEY, activeLanguage); } catch (error) { /* ignore */ }
      apply();
      return activeLanguage;
    };

    return {
      get language() { return activeLanguage; },
      t,
      apply,
      setLanguage,
    };
  }

  return { createI18n, normalizeLanguage };
